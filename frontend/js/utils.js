/** Format a token count compactly: 43644 -> "44k", 950 -> "950". */
export function tokensShort(n) {
  if (n == null) return "?";
  return n >= 1000 ? Math.round(n / 1000) + "k" : String(n);
}

/** Percentage of context window still free, clamped 0–100. */
export function ctxLeftPct(ctx) {
  if (!ctx || !ctx.limit) return null;
  return Math.max(0, Math.min(100, Math.round((1 - ctx.tokens / ctx.limit) * 100)));
}
