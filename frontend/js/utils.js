/** Shorthand for document.getElementById. */
export const $ = (id) => document.getElementById(id);

/** Escape a string for safe insertion into innerHTML. */
export function esc(str) {
  return String(str).replace(/[&<>"']/g, (c) => ({
    "&": "&amp;", "<": "&lt;", ">": "&gt;", "\"": "&quot;", "'": "&#39;",
  }[c]));
}

/** Human-readable age from an epoch-ms timestamp. */
export function age(ms) {
  if (!ms) return "?";
  const s = Math.floor((Date.now() - ms) / 1000);
  if (s < 60) return s + "s";
  const m = Math.floor(s / 60);
  if (m < 60) return m + "m";
  const h = Math.floor(m / 60);
  if (h < 24) return h + "h " + (m % 60) + "m";
  const d = Math.floor(h / 24);
  return d + "d " + (h % 24) + "h";
}

/** Show a transient status message in a `.msg` element. Auto-clears on success. */
export function flash(el, text, ok) {
  el.textContent = text;
  el.className = "msg " + (ok ? "ok" : "err");
  if (ok) setTimeout(() => {
    if (el.textContent === text) { el.textContent = ""; el.className = "msg"; }
  }, 3000);
}

